Protocol.CamAddrProcess = function(s, addr)
	local gg = math.mod(addr, 10) + 0x30;
	addr = math.floor(addr / 10);
	local ss = math.mod(addr, 10) + 0x30;
	addr = math.floor(addr / 10);
	local bb = math.mod(addr, 10) + 0x30;
	s[6] = gg;
	s[5] = ss;
	s[4] = bb;
	return s;
end;

