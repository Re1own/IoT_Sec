Protocol.Checksum = function (s)
	return s;
end;


Protocol.CamAddrProcess = function(s, addr)
	local newaddr = math.mod(addr,8);
              s[1] = newaddr + 128;
	return s;
end;

Protocol.SpeedProcess = function(s, arg1, arg2)
                 s[6] = arg1;
                 s[5] = arg2;
                 return s;
end;

